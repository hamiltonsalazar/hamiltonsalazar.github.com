fn main() {
 // TIPOS DE DATOS

 // Númericos
 // i8, i16, i32, i64, i28 -> Con signo -+
 // u8, u16, u32, u64, u128 -> Sin signo +

 let numero_uno: i8 = -10;
 let numero_dos: u8 = 10;

 // Char
 let caracter = 'a';

 // Float
 let real: f32 = 12.5;

 // Boolean 
 let resultado: bool = false;
 
 println!("Número Uno: {}.\nNúmero Dos: {}.\nCarácter: {}.\nNúmero décimal: {}. Valor Boolean: {}", numero_uno, numero_dos, caracter, real, resultado);

}
